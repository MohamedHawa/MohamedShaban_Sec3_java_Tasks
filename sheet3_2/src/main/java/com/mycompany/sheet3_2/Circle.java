
package com.mycompany.sheet3_2;




public class Circle {
    
   
   
    public double  Get_Circle_area(double radius_circle)
    {
        
      return  (radius_circle*(3.14*3.14));
        
    }
    
      public double  Get_Circle_circumference(double radius_circle)
    {
        
      return  (2*3.14*radius_circle);
        
    }
    
    
    
}

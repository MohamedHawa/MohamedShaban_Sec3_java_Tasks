
package com.mycompany.sheet3_2;


public class Triangle {
    
    
 
    
      public double  Get_Triangle_area(double length_triangle,double width_triangel)
    {
        
      return   (length_triangle*width_triangel);
        
    }
    
      public double  Get_Triangle_circumference(double length_triangle,double width_triangel)
    {
        
      return      2*(length_triangle+width_triangel);
        
    }
    
    
    
}
